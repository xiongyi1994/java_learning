package basic.data.type;

public class CharLearning {
	public static void main(String[] args) {
		char a = 'A';
    System.out.println(a); // A
		System.out.println(a + 0); // 65
		System.out.println(a + "A");// AA
    System.out.println(a + 'A');// 130
		char b = '1';
    System.out.println(b + 0); // 49
		System.out.println(b + "A"); // 1A
    System.out.println(b + '1'); // 98
	}
}
